package basic_0818;

public class Printer {
    static void println(int num) {
        System.out.println(num);
    }

    static void println(boolean num) {
        System.out.println(num);
    }

    static void println(double num) {
        System.out.println(num);
    }

    static void println(String num) {
        System.out.println(num);
    }
}
